export const getRozetkaParamsForCategory = (categoryId) => {
    const key = String(categoryId || '').trim();
    return ROZETKA_PARAMS_BY_CATEGORY[key] || null;
};

export const getRozetkaParamNames = (categoryId) => {
    const config = getRozetkaParamsForCategory(categoryId);
    return config ? Object.keys(config) : [];
};

export const getRozetkaParamValues = (categoryId, paramName) => {
    const config = getRozetkaParamsForCategory(categoryId);
    if (!config || !paramName) return [];
    return config[paramName] || [];
};
